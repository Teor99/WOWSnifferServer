    public static Opcode getOpcodeByValue(int value) {
        if (map == null) {
            initMap();
        }

        return map.getOrDefault(value, UNKNOWN);
    }

    private static void initMap() {
        map = new HashMap<>();
        for (Opcode opcode : values()) {
            map.put(opcode.value, opcode);
        }
    }

    @Override
    public String toString() {
        return name() + " (0x" + Integer.toHexString(value) + ")";
    }
